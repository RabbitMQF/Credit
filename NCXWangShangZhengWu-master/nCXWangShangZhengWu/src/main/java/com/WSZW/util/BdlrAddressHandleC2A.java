	public static String getAddressName(String code) {
		List<Map<String, Object>> list = getAddressList(code);
		for (int i = 0; i < list.size(); i++) {
			if (list.get(i).get("code").equals(code)) {
				return getFrontName(code) + (String) list.get(i).get("name");
			}
		}
		return "null";
	}

